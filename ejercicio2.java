public class ejercicio2 {

    public static void main(String[] args) {
        // Mayor número representable en char
        char mayorChar = Character.MAX_VALUE;
        System.out.println("Mayor número representable en char: " + (int) mayorChar);

        // Mayor número representable en short
        short mayorShort = Short.MAX_VALUE;
        System.out.println("Mayor número representable en short: " + mayorShort);

        // Mayor número representable en int
        int mayorInt = Integer.MAX_VALUE;
        System.out.println("Mayor número representable en int: " + mayorInt);
    }
}
